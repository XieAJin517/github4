package bean;

public class Warehose {
private int id;
private String name;
private String position;
private int shelves_total;
private int state;
private String specification;
/**
 * @return id
 */
public int getId() {
	return id;
}
/**
 * @param id 要设置的 id
 */
public void setId(int id) {
	this.id = id;
}
/**
 * @return name
 */
public String getName() {
	return name;
}
/**
 * @param name 要设置的 name
 */
public void setName(String name) {
	this.name = name;
}
/**
 * @return position
 */
public String getPosition() {
	return position;
}
/**
 * @param position 要设置的 position
 */
public void setPosition(String position) {
	this.position = position;
}
/**
 * @return shelves_total
 */
public int getShelves_total() {
	return shelves_total;
}
/**
 * @param shelves_total 要设置的 shelves_total
 */
public void setShelves_total(int shelves_total) {
	this.shelves_total = shelves_total;
}
/**
 * @return state
 */
public int getState() {
	return state;
}
/**
 * @param state 要设置的 state
 */
public void setState(int state) {
	this.state = state;
}
/**
 * @return specification
 */
public String getSpecification() {
	return specification;
}
/**
 * @param specification 要设置的 specification
 */
public void setSpecification(String specification) {
	this.specification = specification;
}



}
