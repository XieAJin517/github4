package bean;

public class Supplier {
private int supplierID;
private String suppliername;
private String relaname;
private String phone;
private String address;
private String zipcode;
private String pwd;
private String descriptio;
private String businesslicens;
/**
 * @return supplierID
 */
public int getSupplierID() {
	return supplierID;
}
/**
 * @param supplierID 要设置的 supplierID
 */
public void setSupplierID(int supplierID) {
	this.supplierID = supplierID;
}
/**
 * @return suppliername
 */
public String getSuppliername() {
	return suppliername;
}
/**
 * @param suppliername 要设置的 suppliername
 */
public void setSuppliername(String suppliername) {
	this.suppliername = suppliername;
}
/**
 * @return relaname
 */
public String getRelaname() {
	return relaname;
}
/**
 * @param relaname 要设置的 relaname
 */
public void setRelaname(String relaname) {
	this.relaname = relaname;
}
/**
 * @return phone
 */
public String getPhone() {
	return phone;
}
/**
 * @param phone 要设置的 phone
 */
public void setPhone(String phone) {
	this.phone = phone;
}
/**
 * @return address
 */
public String getAddress() {
	return address;
}
/**
 * @param address 要设置的 address
 */
public void setAddress(String address) {
	this.address = address;
}
/**
 * @return zipcode
 */
public String getZipcode() {
	return zipcode;
}
/**
 * @param zipcode 要设置的 zipcode
 */
public void setZipcode(String zipcode) {
	this.zipcode = zipcode;
}
/**
 * @return pwd
 */
public String getPwd() {
	return pwd;
}
/**
 * @param pwd 要设置的 pwd
 */
public void setPwd(String pwd) {
	this.pwd = pwd;
}
/**
 * @return descriptio
 */
public String getDescriptio() {
	return descriptio;
}
/**
 * @param descriptio 要设置的 descriptio
 */
public void setDescriptio(String descriptio) {
	this.descriptio = descriptio;
}
/**
 * @return businesslicens
 */
public String getBusinesslicens() {
	return businesslicens;
}
/**
 * @param businesslicens 要设置的 businesslicens
 */
public void setBusinesslicens(String businesslicens) {
	this.businesslicens = businesslicens;
}

}
