package bean;

public class Warenum {
	private int proid;
	private String name;
	private int latid;
	private int warenum;
	/**
	 * @return proid
	 */
	public int getProid() {
		return proid;
	}
	/**
	 * @param proid 要设置的 proid
	 */
	public void setProid(int proid) {
		this.proid = proid;
	}
	/**
	 * @return name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name 要设置的 name
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return latid
	 */
	public int getLatid() {
		return latid;
	}
	/**
	 * @param latid 要设置的 latid
	 */
	public void setLatid(int latid) {
		this.latid = latid;
	}
	/**
	 * @return warenum
	 */
	public int getWarenum() {
		return warenum;
	}
	/**
	 * @param warenum 要设置的 warenum
	 */
	public void setWarenum(int warenum) {
		this.warenum = warenum;
	}


}
