package bean;

public class Lattice {
	private int latid;
	private int sheid;
	private int state;
	private String specification;
	/**
	 * @return latid
	 */
	public int getLatid() {
		return latid;
	}
	/**
	 * @param latid 要设置的 latid
	 */
	public void setLatid(int latid) {
		this.latid = latid;
	}
	/**
	 * @return sheid
	 */
	public int getSheid() {
		return sheid;
	}
	/**
	 * @param sheid 要设置的 sheid
	 */
	public void setSheid(int sheid) {
		this.sheid = sheid;
	}
	/**
	 * @return state
	 */
	public int getState() {
		return state;
	}
	/**
	 * @param state 要设置的 state
	 */
	public void setState(int state) {
		this.state = state;
	}
	/**
	 * @return specification
	 */
	public String getSpecification() {
		return specification;
	}
	/**
	 * @param specification 要设置的 specification
	 */
	public void setSpecification(String specification) {
		this.specification = specification;
	}

}
